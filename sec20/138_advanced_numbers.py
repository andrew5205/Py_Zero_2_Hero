# built-in
# hex()
# oct()
# bin()
# pow()
# abs()
#round()


print(hex(512))         # 0x200

print(oct(512))         # 0o1000

print(bin(1024))        # 0b10000000000


print(pow(2, 3))        # 8
# pow(x ** y [% z])
print(pow(2, 4, 3))     # 1


print(abs(-2))          # 2

print(round(3.9))       # 4
print(round(3.1415926)) # 3








