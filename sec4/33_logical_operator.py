
# and => &
# or => |
# not => !


print((1 < 2) and (2 < 3))      # True
print((1 < 2) & (2 < 3))        # True


print((1 < 2) or ("a" == "b"))      # True
print((1 < 2) | ("a" < "b"))        # True

print("a" < "b")                    # True      # ASCII 97 < 98
print("a" > "b")                    # False     # ASCII


print(not True)     # False
print(20 != 30)     # True
print( 0 == 0)      # True  # differ then JS

